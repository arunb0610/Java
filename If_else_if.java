class If_else_if
{    
public static void main(String[] args)
{    
int number = -13; 
   
    if(number>0)
    {  
        System.out.println(number+" is Positive number");  
    }
    else if(number<0)
    {  
        System.out.println(number+" is Negative number");  
    }
    else
    {  
        System.out.println("The number is zero");  
    }  
}    
} 
